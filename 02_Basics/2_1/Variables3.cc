#include <iostream>

int main()
{
    // 1 Byte
    bool my_bool1 = true;
    bool my_bool2 = false;

    // 1 Byte = 8 Bit (2^8 = 256)
    char my_char = 10;

    // 2 Byte = 16 Bit (2^16 ~ 65.000)
    short my_short = 300;

    // 4 Byte = 32 Bit (2^32 ~ 4.000.000.000)
    int my_int = 100'000;

    // 8 Byte = 64 Bit (2^64)
    long long my_ll = 5'000'000'000;

    // 4 Byte = 32 Bit
    float my_float = 10.0;

    // 8 Byte = 64 Bit
    double my_dbl = 10.0;

    return 0;
}
